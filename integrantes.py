#!/usr/bin/env python3
# Script que muestra los nombres y apellidos de los integrantes del grupo

integrantes = [
    {"nombre": "Antonia", "apellido": "Silva"},
    {"nombre": "Jorge", "apellido": "Cornejos"},
]

print("=== Integrantes del Grupo ===")
for i, integrante in enumerate(integrantes, 1):
    print(f"{i}. {integrante['nombre']} {integrante['apellido']}")
